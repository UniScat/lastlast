package team.shop.DTO;

public class productVO {
	private int pNum;
	private String pName;
	private int pPrice;
	private String pImg;
	private String pShortInfo;
	private String pDetailInfo;
	private String pCategory;
	
	public int getpNum() {
		return pNum;
	}
	public void setpNum(int pNum) {
		this.pNum = pNum;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public int getpPrice() {
		return pPrice;
	}
	public void setpPrice(int pPrice) {
		this.pPrice = pPrice;
	}
	public String getpImg() {
		return pImg;
	}
	public void setpImg(String pImg) {
		this.pImg = pImg;
	}
	public String getpShortInfo() {
		return pShortInfo;
	}
	public void setpShortInfo(String pShortInfo) {
		this.pShortInfo = pShortInfo;
	}
	public String getpDetailInfo() {
		return pDetailInfo;
	}
	public void setpDetailInfo(String pDetailInfo) {
		this.pDetailInfo = pDetailInfo;
	}
	public String getpCategory() {
		return pCategory;
	}
	public void setpCategory(String pCategory) {
		this.pCategory = pCategory;
	}
	
	
	
}
